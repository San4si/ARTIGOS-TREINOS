/*DOM é a sigla em inglês para Document Object Model, 
que significa modelo de documento por objetos. 
Apesar de se tratar de um assunto complexo, 
ele já está embutido dentro dos navegadores. 
O que significa que a parte em que os programadores 
têm acesso é bem mais simples e fácil de manipular.

É uma interface padronizada que permite que navegadores e 
scripts possam manipular o conteúdo de uma página web sem 
a necessidade de realizar atualizações e upgrades. É ele, 
ainda, que põe ordem na navegação web para que o conteúdo 
fique acessível a todos.

*/